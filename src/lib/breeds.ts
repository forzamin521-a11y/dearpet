/** species 미지정 시 강아지+고양이 전체에서 검색 */
export function searchBreeds(
  query: string,
  species?: "dog" | "cat",
  limit = 8
): string[] {
  const q = query.trim();
  if (!q) return [];
  const pool =
    species === "dog"
      ? DOG_BREEDS
      : species === "cat"
        ? CAT_BREEDS
        : [...DOG_BREEDS, ...CAT_BREEDS];
  // 시작 일치 우선, 그다음 부분 일치
  const starts = pool.filter((b) => b.startsWith(q));
  const includes = pool.filter((b) => !b.startsWith(q) && b.includes(q));
  return [...starts, ...includes].slice(0, limit);
}
